/** because "dashing" implies speed */
type Dasher = "💨";

/** representing dancing or grace */
type Dancer = "💃";

/** a deer, prancing */
type Prancer = "🦌";

/** a star for the dazzling, slightly mischievous Vixen */
type Vixen = "🌟";

/** for the celestial body that shares its name */
type Comet = "☄️";

/** symbolizing love, as Cupid is the god of love */
type Cupid = "❤️";

/** representing thunder, as "Donner" means thunder in German */
type Donner = "🌩️";

/** meaning lightning in German, hence the lightning bolt */
type Blitzen = "⚡";

/** for his famous red nose */
type Rudolph = "🔴";

type Reindeer =
  | Dasher
  | Dancer
  | Prancer
  | Vixen
  | Comet
  | Cupid
  | Donner
  | Blitzen
  | Rudolph;

type SudokuRow = [
  [Reindeer, Reindeer, Reindeer],
  [Reindeer, Reindeer, Reindeer],
  [Reindeer, Reindeer, Reindeer],
];

type SudokuBoard = [
  SudokuRow,
  SudokuRow,
  SudokuRow,
  SudokuRow,
  SudokuRow,
  SudokuRow,
  SudokuRow,
  SudokuRow,
  SudokuRow,
];

type Filter<Row, Char> = Row extends [infer First, ...infer Rest]
  ? First extends Char
    ? [First, ...Filter<Rest, Char>]
    : Filter<Rest, Char>
  : [];

type ValidateRowUniqueness<
  Arr extends any[],
  Expanded = [...Arr[0], ...Arr[1], ...Arr[2]],
  Duplicates extends any[] = [],
> = Expanded extends [infer First, ...infer Rest]
  ? ValidateRowUniqueness<
      Arr,
      Rest,
      [...Duplicates, ...Filter<Expanded, First>]
    >
  : Duplicates["length"] extends 9
    ? true
    : false;

type AreAllValidReindeers<
  Row extends any[],
  Expanded extends any[] = [...Row[0], ...Row[1], ...Row[2]],
> = Expanded[number] extends Reindeer
  ? Reindeer extends Expanded[number]
    ? true
    : false
  : false;

type ValidateRow<Row extends any[]> = [
  ValidateRowUniqueness<Row>,
  AreAllValidReindeers<Row>,
] extends [true, true]
  ? true
  : false;

type ValidateRegions<Board extends SudokuBoard> = [
  ValidateRow<[Board[0][0], Board[1][0], Board[2][0]]>,
  ValidateRow<[Board[3][0], Board[4][0], Board[5][0]]>,
  ValidateRow<[Board[6][0], Board[7][0], Board[8][0]]>,
  ValidateRow<[Board[0][1], Board[1][1], Board[2][1]]>,
  ValidateRow<[Board[3][1], Board[4][1], Board[5][1]]>,
  ValidateRow<[Board[6][1], Board[7][1], Board[8][1]]>,
  ValidateRow<[Board[0][2], Board[1][2], Board[2][2]]>,
  ValidateRow<[Board[3][2], Board[4][2], Board[5][2]]>,
  ValidateRow<[Board[6][2], Board[7][2], Board[8][2]]>,
][number];

type ValidateRows<Board, Test extends any[] = []> = Board extends [
  infer First extends SudokuRow,
  ...infer Rest,
]
  ? ValidateRows<Rest, [...Test, ValidateRow<First>]>
  : Test extends [true, true, true, true, true, true, true, true, true]
    ? true
    : false;

type GetColumn<
  Board,
  ColumnIdx extends number,
  RA extends SudokuRow[] = [],
> = Board extends [infer First extends any[], ...infer Rest]
  ? GetColumn<
      Rest,
      ColumnIdx,
      [...RA, [...First[0], ...First[1], ...First[2]][ColumnIdx]]
    >
  : [[RA[0], RA[1], RA[2]], [RA[3], RA[4], RA[5]], [RA[6], RA[7], RA[8]]];

type ValidateColumn<Board, ColumnIdx extends number> = ValidateRow<
  GetColumn<Board, ColumnIdx>
>;

type ValidateColumns<
  Board extends SudokuBoard,
  Test extends boolean[] = [],
> = Test["length"] extends 9
  ? Test[number]
  : ValidateColumns<Board, [...Test, ValidateColumn<Board, Test["length"]>]>;

type Validate<Board extends SudokuBoard> = [
  ValidateRows<Board>,
  ValidateColumns<Board>,
  ValidateRegions<Board>,
] extends [true, true, true]
  ? true
  : false;

